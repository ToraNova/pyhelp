#!/usr/bin/python3

'''
python3 data types

basic types
'''

astr = 'this is a string'
print(astr,type(astr))

aint = 3
print(aint,type(aint))

aflt = 3.0
print(aflt,type(aflt))

acmp = 3.0+2j
print(acmp,type(acmp))

alst = [1,2,3]
print(alst,type(alst))

atpl = (1,2,3)
print(atpl,type(atpl))

arng = range(3)
print(arng,type(arng))

adct = {"a":1,"b":2}
print(adct,type(adct))

aset = {1,2,3}
print(aset,type(aset))

aboo = False
print(aboo,type(aboo))

abyt = b'this is a byte string'
print(abyt,type(abyt))

abar = bytearray(5)
print(abar,type(abar))
